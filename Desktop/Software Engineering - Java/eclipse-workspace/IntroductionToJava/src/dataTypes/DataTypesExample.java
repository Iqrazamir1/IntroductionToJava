package dataTypes;

public class DataTypesExample {
	public static void main(String[] args) {
		
		String name = "Iqra"; // String
		
		byte Numbers = 2; // Numbers
		short ShortNumbers = 20;
		int Integer = 4245;
		long LongNumbers = 45115214524L;
		
		float FloatNumber = 12.55f; // Float Numbers
		double DoubleNumber = 452.55;
		
		char Letters = 'Y'; // Characters
		boolean TrueOrFalse = true;
		
		System.out.println(name); // Print statement
		System.out.println(Numbers);
		System.out.println(ShortNumbers);
		System.out.println(Integer);
		System.out.println(LongNumbers);
		System.out.println(FloatNumber);
		System.out.println(DoubleNumber);
		System.out.println(Letters);
		System.out.println(TrueOrFalse);
	}

}
