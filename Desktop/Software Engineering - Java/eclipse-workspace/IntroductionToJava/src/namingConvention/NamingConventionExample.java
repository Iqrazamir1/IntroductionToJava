package namingConvention;

public class NamingConventionExample {
	public static void main(String[] args) {
		System.out.println("UpperCamelCase: Each new word has a capitcal letter.");
		System.out.println("lowerCamelCase: Same as UpperCamelCase but the first word is always lower case.");
		System.out.println("snake_case: All lower case but seperated by underscores.");
		System.out.println("UPPERCASE: All uppercase.");
		System.out.println("lowercase: All lowercase.");
	}
}
